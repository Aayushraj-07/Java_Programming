// Widening
// 1. Widening in programming refers to the process of converting a data type to a larger or more general type without losing data.
// 2. it is also known as implicit type casting

package com.variables;

public class Widening {
    public static void main(String[] args) {
        int a = 10;
        float b =10; // widening int to float
        System.out.println(a);
        System.out.println(b);
    }
}

